-- ============================================================
-- 五、检测数据表
-- ============================================================
CREATE TABLE `inspection_record` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `project_id` BIGINT NOT NULL COMMENT '关联项目',

    -- 基础信息
    `construction_unit` VARCHAR(200) DEFAULT NULL COMMENT '施工单位',
    `weld_no` VARCHAR(100) DEFAULT NULL COMMENT '焊口编号',
    `instruction_no` VARCHAR(100) DEFAULT NULL COMMENT '指令编号',
    `instruction_date` DATE DEFAULT NULL COMMENT '指令日期',
    `inspection_method` VARCHAR(20) DEFAULT NULL COMMENT '检测方法 RT/UT/PT/MT/AUT/PA/TOFD/DR',
    `project_name` VARCHAR(200) DEFAULT NULL COMMENT '工程名称',
    `unit_project_name` VARCHAR(200) DEFAULT NULL COMMENT '单位工程名称',
    `bu_dept` VARCHAR(100) DEFAULT NULL COMMENT '所属事业部/项目部',
    `specification` VARCHAR(100) DEFAULT NULL COMMENT '规格',
    `material` VARCHAR(100) DEFAULT NULL COMMENT '材质',
    `groove_type` VARCHAR(50) DEFAULT NULL COMMENT '坡口形式',
    `position` VARCHAR(100) DEFAULT NULL COMMENT '部位',
    `welding_method` VARCHAR(100) DEFAULT NULL COMMENT '焊接方式',
    `ratio` VARCHAR(50) DEFAULT NULL COMMENT '比例',
    `inspection_standard` VARCHAR(100) DEFAULT NULL COMMENT '检测标准',
    `qualified_level` VARCHAR(50) DEFAULT NULL COMMENT '合格级别',
    `inspection_item` VARCHAR(200) DEFAULT NULL COMMENT '检测项目',
    `welder_code` VARCHAR(50) DEFAULT NULL COMMENT '焊工代号',
    `welding_dept` VARCHAR(200) DEFAULT NULL COMMENT '焊接部门',
    `inspection_length` DECIMAL(10,2) DEFAULT NULL COMMENT '检测长度(m)',
    `process_card_no` VARCHAR(100) DEFAULT NULL COMMENT '工艺卡编号',
    `sampling_instruction_no` VARCHAR(100) DEFAULT NULL COMMENT '抽检指令编号',
    `inspection_date` DATE DEFAULT NULL COMMENT '检测日期',

    -- 检测结果
    `result_level` VARCHAR(10) DEFAULT NULL COMMENT '级别 Ⅰ/Ⅱ/Ⅲ/Ⅳ',
    `inspection_conclusion` VARCHAR(20) DEFAULT NULL COMMENT '检测结论 合格/不合格',
    `unqualified_handling` VARCHAR(500) DEFAULT NULL COMMENT '不合格处理情况',

    -- 报告中缺陷
    `report_defect_position` VARCHAR(200) DEFAULT NULL COMMENT '报告中缺陷位置',
    `report_defect_nature` VARCHAR(200) DEFAULT NULL COMMENT '报告中缺陷性质',
    `report_defect_length` DECIMAL(10,2) DEFAULT NULL COMMENT '报告中缺陷长度',
    `unqualified_defect_type` VARCHAR(200) DEFAULT NULL COMMENT '不合格缺陷类型',

    -- 备注/人员
    `remark` VARCHAR(500) DEFAULT NULL COMMENT '备注',
    `inspector_name` VARCHAR(50) DEFAULT NULL COMMENT '检测人员',

    -- 底片(RT)
    `box_no` VARCHAR(50) DEFAULT NULL COMMENT '箱号',
    `film_length` DECIMAL(10,2) DEFAULT NULL COMMENT '底片长度',
    `film_count` INT DEFAULT NULL COMMENT '底片张数',

    -- 文件
    `image_url` VARCHAR(500) DEFAULT NULL COMMENT '图片',
    `inspection_report_url` VARCHAR(500) DEFAULT NULL COMMENT '检测报告文件',
    `original_record_url` VARCHAR(500) DEFAULT NULL COMMENT '原始记录文件',

    -- 缺陷位置(JSON, 最多15组)
    `defect_positions` JSON DEFAULT NULL COMMENT '缺陷位置数组 [{"pos":1,"defect":"气孔","length":5.2,"level":"Ⅱ","other":"..."}]',

    -- Ⅰ-Ⅳ级缺陷统计 (底片评定用)
    `level_i` INT DEFAULT 0 COMMENT 'Ⅰ级缺陷数',
    `level_ii` INT DEFAULT 0 COMMENT 'Ⅱ级缺陷数',
    `level_iii` INT DEFAULT 0 COMMENT 'Ⅲ级缺陷数',
    `level_iv` INT DEFAULT 0 COMMENT 'Ⅳ级缺陷数',

    `create_by` BIGINT DEFAULT NULL,
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `deleted` TINYINT DEFAULT 0,
    PRIMARY KEY (`id`),
    INDEX `idx_project_id` (`project_id`),
    INDEX `idx_weld_no` (`weld_no`),
    INDEX `idx_inspection_method` (`inspection_method`),
    INDEX `idx_inspection_date` (`inspection_date`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='检测数据表';

