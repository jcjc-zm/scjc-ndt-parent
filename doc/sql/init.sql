CREATE TABLE `sys_dept` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `parent_id` BIGINT DEFAULT NULL COMMENT '父节点ID',
    `dept_name` VARCHAR(100) NOT NULL COMMENT '部门名称',
