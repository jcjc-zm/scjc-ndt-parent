-- ============================================================
-- 八、报告记录表
-- ============================================================
CREATE TABLE `report_record` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `inspection_id` BIGINT NOT NULL COMMENT '关联检测记录',
    `template_id` BIGINT NOT NULL COMMENT '关联模板',
    `report_no` VARCHAR(100) NOT NULL COMMENT '报告编号',
    `report_content` JSON DEFAULT NULL COMMENT '模板填充后的实际内容',
    `image_selections` JSON DEFAULT NULL COMMENT '用户选择的图片 [{area_id, image_id, image_url}]',
    `status` VARCHAR(20) DEFAULT 'DRAFT' COMMENT 'DRAFT/PENDING_SIGN/SIGNED/REJECTED',
    `pdf_url` VARCHAR(500) DEFAULT NULL COMMENT '导出PDF路径',
    `create_by` BIGINT DEFAULT NULL,
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `deleted` TINYINT DEFAULT 0,
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_report_no` (`report_no`),
    INDEX `idx_inspection_id` (`inspection_id`),
    INDEX `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='报告记录表';

-- ============================================================
-- 九、签字记录表
-- ============================================================
CREATE TABLE `signature_record` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `report_id` BIGINT NOT NULL COMMENT '关联报告',
    `signatory_id` BIGINT DEFAULT NULL COMMENT '签字人',
    `signatory_role` VARCHAR(50) NOT NULL COMMENT '签字角色 TECHNICAL_LEADER/PROJECT_MANAGER',
    `sign_order` INT NOT NULL COMMENT '签字顺序(1,2)',
    `sign_status` VARCHAR(20) DEFAULT 'PENDING' COMMENT 'PENDING/SIGNED/REJECTED',
    `sign_time` DATETIME DEFAULT NULL,
    `comment` VARCHAR(500) DEFAULT NULL COMMENT '签字意见',
    `signature_image_url` VARCHAR(500) DEFAULT NULL COMMENT '签名图片',
    PRIMARY KEY (`id`),
    INDEX `idx_report_id` (`report_id`),
    INDEX `idx_signatory_id` (`signatory_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='签字记录表';
